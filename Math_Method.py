import math
x = 5.7
y = 2
b = 10
print("Math Functions Demonstration\n")
print("ceil(x)       =", math.ceil(x))
print("floor(x)      =", math.floor(x))
print("trunc(x)      =", math.trunc(x))
print("fabs(x)       =", math.fabs(x))
print("copysign(x,y) =", math.copysign(x, -y))
print("pow(x, y)     =", math.pow(x, y))
print("sqrt(x)       =", math.sqrt(x))
print("factorial(5)  =", math.factorial(5))
print("log(x)        =", math.log(x))        
print("log(x, b)     =", math.log(x, b))
print("log10(x)      =", math.log10(x))
print("log2(x)       =", math.log2(x))
print("log1p(x)      =", math.log1p(x))
print("exp(x)        =", math.exp(x))
print("expm1(x)      =", math.expm1(x))
print("fmod(x, y)    =", math.fmod(x, y))
print("modf(x)       =", math.modf(x))
print("isfinite(x)   =", math.isfinite(x))
print("isinf(x)      =", math.isinf(x))
print("isnan(x)      =", math.isnan(x))
print("fsum([1,2,3]) =", math.fsum([1, 2, 3]))
print("ldexp(x, 2)   =", math.ldexp(x, 2))
print("acos(0.5)     =", math.acos(0.5))
m, e = math.frexp(x)
print("frexp(x)      = mantissa:", m, "exponent:", e)